package com.sample.web.form;

import com.sample.web.view.Payment;
import com.sample.web.view.Reserve;
import com.sample.web.view.User;

public class PaymentForm {

	private String userId;
	private int pay;
	private String method;
	private String payMethodReal;
	private int usedPoint;
	
	public PaymentForm() {
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getPay() {
		return pay;
	}

	public void setPay(int pay) {
		this.pay = pay;
	}

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	
	public String getPayMethodReal() {
		return payMethodReal;
	}

	public void setPayMethodReal(String payMethodReal) {
		this.payMethodReal = payMethodReal;
	}

	public int getUsedPoint() {
		return usedPoint;
	}

	public void setUsedPoint(int usedPoint) {
		this.usedPoint = usedPoint;
	}

	@Override
	public String toString() {
		return "PaymentForm [userId=" + userId + ", pay=" + pay + ", method=" + method + ", payMethodReal="
				+ payMethodReal + ", usedPoint=" + usedPoint + "]";
	}


	
	
}
